        еңбекақысы өте төмен, неге министрге жазбайсыздар? Мысалы менің қызым сол үшін жұмыс жасамаймын
        деп отыр.</p>
    <p class="my-title text-left"><b>Жауап:</b> Құрметті Зауре Берікқалиқызы! Сіздің көтеріп отырған мәселе өте
