    <div class="news-full"><p style="text-align: justify;">Гимн – мемлекеттің басты рәміздерінің бірі. Гректің «gimneo»
            сөзінен шыққан «гимн» термині «салтанатты ән» деген мағынаны білдіреді. Гимн ел азаматтарын тиімді
            әлеуметтік-саяси тұрғыдан топтастырып, этномәдени тұрғыдан теңдестіру үшін негізгі мәнге ие, маңызды
            дыбыстық рәміз саналады.</p>
        <p style="text-align: justify;">Тәуелсіз Қазақстанның тарихында еліміздің мемлекеттік гимні екі рет – 1992 және
            2006 жылдары бекітілді.</p>
        <p style="text-align: justify;">Республика мемлекеттік егемендігін иеленгеннен кейін, 1992 жылы Қазақстан
            әнұранының музыкасы мен мәтініне байқау жарияланды. Байқау қорытындысы бойынша Қазақ КСР гимнінің музыкалық
            редакциясын сақтау туралы шешім қабылданды. Осылайша тәуелсіз Қазақстанның алғашқы гимінің музыкасының
            авторлары Мұқан Төлебаев, Евгений Брусиловский және Латиф Хамиди болды. Сонымен қатар, үздік мәтінге
            жарияланған байқауда авторлар ұжымы, белгілі ақындар Мұзафар Әлімбаев, Қадыр Мырзалиев, Тұманбай Молдағалиев
            және Жадыра Дәрібаева жеңіп шықты.</p>
        <p style="text-align: justify;">Елдің дыбыстық рәмізінің танымалдығын арттыру мақсатында 2006 жылы жаңа
            мемлекеттік гимн&nbsp;қабылданды. Оның негізі ретінде халықтың арасында кеңінен танымал «Менің Қазақстаным»
            патриоттық әні таңдап алынды. Ол әнді Шәмші Қалдаяқов 1956 жылы Жұмекен Нәжімеденовтің сөзіне жазған
            болатын. Қазақстан Президенті Нұрсұлтан Назарбаев әнге мемлекеттік гимн жоғары мәртебесін беру және
            анағұрлым салтанатты шырқалуы үшін музыкалық туындының бастапқы мәтінін өңдеді. Қазақстан Парламенті 2006
            жылы 6 қаңтарда палаталардың бірлескен отырысында «Мемлекеттік рәміздер туралы» Жарлыққа тиісті түзету
            енгізіп, еліміздің жаңа мемлекеттік гимнін&nbsp;бекітті.</p>
